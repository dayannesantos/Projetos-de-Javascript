let firstNumber = Number(prompt("Informe o primeiro número:"));
let secondNumber = Number(prompt("Informe o segundo número:"));

const sum = firstNumber + secondNumber;
const subtraction = firstNumber - secondNumber;
const multiplication = secondNumber * firstNumber;
const division = secondNumber / secondNumber;

alert(
  `Resultados: \nSoma: ${sum} \nSubtração: ${subtraction} \nMultiplicação: ${multiplication} \nDivisão: ${division}`
);
